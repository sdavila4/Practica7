public class Pajaro {
    private int Daño;
    private String Poder;
    private String Color;

    public Pajaro(int Daño, String Poder, String Color) {
        this.Daño = Daño;
        this.Poder = Poder;
        this.Color = Color;
    }
    public int getDaño() {
        return Daño;
    }
    public String getPoder() {
        return Poder;
    }
    public String getColor() {
        return Color;
    }

}